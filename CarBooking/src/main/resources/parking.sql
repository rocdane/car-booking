CREATE TABLE IF NOT EXISTS Car(
	CarId INTEGER(10) AUTO_INCREMENT NOT NULL,
	Immatriculation VARCHAR(10),
	Marque VARCHAR(20),
	Modele VARCHAR(20),
	Description VARCHAR(100),
	Price INTEGER(10),
	PRIMARY KEY(CarId),
	isRent BOOLEAN,
	IMAGE VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS Customer(
	CustomerId INTEGER(10) AUTO_INCREMENT NOT NULL,
	Cni INTEGER(10),
	Nom VARCHAR(10),
	Prenom VARCHAR(20),
	Phone VARCHAR(12),
	PRIMARY KEY (CustomerId)
);

CREATE TABLE IF NOT EXISTS History(
	HistoryID INTEGER(10) AUTO_INCREMENT NOT NULL,
	DateOf TIMESTAMP,
	DateTo TIMESTAMP,
	Car INTEGER(10),
	Customer INTEGER(10),
	PRIMARY KEY(HistoryID),
	CONSTRAINT HistoryCar FOREIGN KEY(Car) REFERENCES Car(CarId),
	CONSTRAINT HistoryCustomer FOREIGN KEY(Customer) REFERENCES Customer(CustomerId)
);

CREATE TABLE IF NOT EXISTS ADMIN(
	AdminId INTEGER(10) AUTO_INCREMENT NOT NULL,
	Username VARCHAR(10),
	Pass VARCHAR(255),
	PRIMARY KEY(AdminId)
);

